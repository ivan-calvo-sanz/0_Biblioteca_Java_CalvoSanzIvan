public class _10_POO_04_Herencia_Clases_Abstractas_Teoria {
    /**
     * ***** CLASES ABSTRACTAS****
     */
    /**
     * Son Clases que no se puede instanciar directamente (no puedes crear
     * objetos de ella), sino que sirve como modelo o plantilla para que otras
     * clases hereden de ella.
     *
     * Se usa cuando quieres definir una estructura común y dejar que las
     * subclases concreten ciertos comportamientos.
     */

    /**
     * - Se declara con la palabra clave abstract:
     *
     * - Si una clase hija hereda de una clase abstracta, debe implementar todos
     * sus métodos abstractos, salvo que también sea abstracta.
     */
    /**
     * ¿Cuándo usar clases abstractas?
     *
     * - Cuando quieres compartir código común entre varias clases (atributos y
     * métodos ya definidos).
     *
     * - Cuando quieres forzar a las subclases a implementar ciertos métodos
     * (los abstractos).
     *
     */
}
